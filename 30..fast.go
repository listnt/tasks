package main

import "fmt"

func main() {
	a := []string{"A", "B", "C", "D", "E"}
	i := 2

	// Убиарем элемент i из массива
	a[i] = a[len(a)-1] // Целевой элемент затираем последним
	a[len(a)-1] = ""   // Затираем последний элемент пустым
	a = a[:len(a)-1]   // Возвращаем слайс минус последний элемент

	fmt.Println(a) // [A B E D]
}
